"""Operator's switchboard — web UI for gradphone.

Two roles, one dashboard template:

- **Operator** logs in at /ui/login with BRIDGE_API_KEY. Unrestricted view
  of all tenants + ability to manage tenants (deactivate, override quota).
- **Tenant** lands via /ui/auth?token=<magic-link>. Sees only their own
  calls and quota. Dial form auto-scopes to their tenant_id.

Magic links are generated by the Telegram bot's /web command (the bot
signs them with the shared BRIDGE_API_KEY so the bridge can verify
them). Links expire in 5 minutes; the session cookie they set lasts a
week.

Operator-only endpoints (tenant management) sit at /ui/tenants and
/ui/tenant/{id}/update. Tenant-scoped data routes (/ui/dial,
/ui/history, /ui/calls/live, /ui/result, /ui/audio) work for either
role and self-scope based on session.
"""

from __future__ import annotations

import logging
import os
from pathlib import Path
from typing import Optional

from . import db
import fastapi
from fastapi import Cookie, Form, Request, status
from fastapi.responses import (
    FileResponse,
    HTMLResponse,
    JSONResponse,
    RedirectResponse,
)
from fastapi.templating import Jinja2Templates

from . import ratelimit, tenants, voices
from .business_agent import BusinessCallSpec
from .sessions import (
    COOKIE_MAX_AGE,
    COOKIE_NAME,
    decode_role,
    encode_role,
    is_operator,
    role_tenant_id,
    verify_magic_token,
)

log = logging.getLogger(__name__)

_HERE = Path(__file__).parent
templates = Jinja2Templates(directory=str(_HERE / "templates"))

router = fastapi.APIRouter(prefix="/ui", tags=["ui"])


# Secure cookies by default (the dashboard is served over HTTPS in prod via the
# tunnel / Render). Set INSECURE_COOKIES=1 only for local HTTP dev.
_COOKIE_SECURE = os.environ.get("INSECURE_COOKIES", "").strip().lower() not in (
    "1", "true", "yes",
)


def _set_cookie(response: fastapi.Response, role: str) -> None:
    response.set_cookie(
        COOKIE_NAME,
        encode_role(role),
        httponly=True,
        secure=_COOKIE_SECURE,
        max_age=COOKIE_MAX_AGE,
        samesite="strict",
    )


def _current_role(request: Request) -> Optional[str]:
    return decode_role(request.cookies.get(COOKIE_NAME))


async def _require_session(request: Request) -> str:
    role = _current_role(request)
    if not role:
        raise fastapi.HTTPException(
            status_code=status.HTTP_307_TEMPORARY_REDIRECT,
            headers={"Location": "/ui/login"},
        )
    return role


async def _require_operator(request: Request) -> None:
    role = _current_role(request)
    if not is_operator(role):
        raise fastapi.HTTPException(status_code=403, detail="operator only")


# ─── Auth pages ───────────────────────────────────────────

@router.get("/login", response_class=HTMLResponse)
async def login_form(request: Request, error: Optional[str] = None):
    return templates.TemplateResponse(
        request, "login.html", {"error": error}
    )


@router.post("/login")
async def login_submit(token: str = Form(...)):
    expected = os.environ.get("BRIDGE_API_KEY", "").strip()
    if not expected or token != expected:
        return RedirectResponse(
            "/ui/login?error=invalid+access+token",
            status_code=status.HTTP_303_SEE_OTHER,
        )
    response = RedirectResponse("/ui", status_code=status.HTTP_303_SEE_OTHER)
    _set_cookie(response, "operator")
    return response


@router.get("/auth")
async def magic_auth(token: str):
    """Consume a magic-link token and set a tenant session cookie."""
    role = verify_magic_token(token)
    if not role or not role.startswith("tenant:"):
        return RedirectResponse(
            "/ui/login?error=link+expired+or+invalid",
            status_code=status.HTTP_303_SEE_OTHER,
        )
    tenant_id = role_tenant_id(role)
    if tenant_id is None:
        return RedirectResponse(
            "/ui/login?error=invalid+tenant",
            status_code=status.HTTP_303_SEE_OTHER,
        )
    # Verify the tenant still exists and is active.
    tenant = await tenants.get_tenant_by_id(tenant_id)
    if not tenant or not tenant.get("is_active", 1):
        return RedirectResponse(
            "/ui/login?error=tenant+disabled",
            status_code=status.HTTP_303_SEE_OTHER,
        )
    response = RedirectResponse("/ui", status_code=status.HTTP_303_SEE_OTHER)
    _set_cookie(response, role)
    return response


@router.get("/logout")
async def logout():
    response = RedirectResponse("/ui/login", status_code=status.HTTP_303_SEE_OTHER)
    response.delete_cookie(COOKIE_NAME)
    return response


# ─── Dashboard ───────────────────────────────────────────

@router.get("", response_class=HTMLResponse)
async def dashboard_root(request: Request):
    role = _current_role(request)
    if not role:
        return RedirectResponse("/ui/login", status_code=status.HTTP_303_SEE_OTHER)

    tenant_id = role_tenant_id(role)
    operator = is_operator(role)
    viewer_name = "operator"
    quota_used = None
    quota_limit = None

    if tenant_id is not None:
        tenant = await tenants.get_tenant_by_id(tenant_id)
        if not tenant or not tenant.get("is_active", 1):
            return RedirectResponse(
                "/ui/login?error=tenant+disabled",
                status_code=status.HTTP_303_SEE_OTHER,
            )
        viewer_name = tenant["name"]
        quota_limit = tenant.get("custom_calls_per_day") or ratelimit.DEFAULT_DAILY_QUOTA
        quota_used = await tenants.count_calls_today(tenant_id)

    return templates.TemplateResponse(
        request,
        "dashboard.html",
        {
            "bridge_host": request.headers.get("host", "127.0.0.1:8082"),
            "viewer_name": viewer_name,
            "is_operator": operator,
            "tenant_id": tenant_id,
            "quota_used": quota_used,
            "quota_limit": quota_limit,
        },
    )


@router.get("/", include_in_schema=False)
async def dashboard_slash(request: Request):
    return await dashboard_root(request)


