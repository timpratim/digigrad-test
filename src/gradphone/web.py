@router.get("/calls/live", dependencies=[fastapi.Depends(_require_session)])
async def ui_calls_live(request: Request) -> dict:
    from .bridge import _ACTIVE, _PENDING, _PENDING_LOCK, _snapshot_call

    role = _current_role(request)
    tenant_id = role_tenant_id(role)
    async with _PENDING_LOCK:
        ringing = [_snapshot_call(s, phase_override="ringing") for s in _PENDING.values()]
        active = [_snapshot_call(s) for s in _ACTIVE.values()]
    calls = ringing + active
    if tenant_id is not None:
        calls = [c for c in calls if c.get("tenant_id") == tenant_id]
    calls.sort(key=lambda c: c.get("started_at", 0), reverse=True)
    return {"ok": True, "count": len(calls), "calls": calls}


@router.get("/result/{room}", dependencies=[fastapi.Depends(_require_session)])
async def ui_result(request: Request, room: str) -> dict:
    """Tenant-safe wrapper for /result/{room}. Tenants can only see their own."""
    role = _current_role(request)
    tenant_id = role_tenant_id(role)
    if tenant_id is not None:
        call = await tenants.get_call(room)
        if not call or call.get("tenant_id") != tenant_id:
            return {"status": "missing", "room": room}
    # Delegate to the same logic the public /result uses.
    from .bridge import get_result
    return await get_result(room)


@router.get("/history", dependencies=[fastapi.Depends(_require_session)])
async def ui_history(request: Request, limit: int = 30) -> dict:
    """Tenants: own calls only. Operator: every call across the bridge."""
    role = _current_role(request)
    tenant_id = role_tenant_id(role)
    limit = max(1, min(limit, 200))
    cols = (
        "SELECT room, tenant_id, destination, task, language, business_name, "
        "started_at, ended_at, status, answer, confidence, duration_seconds, "
        "answered_by, twilio_call_status FROM calls "
    )
    if tenant_id is not None:
        rows = await db.fetch_all(
            cols + "WHERE tenant_id = :tid ORDER BY started_at DESC LIMIT :lim",
            tid=tenant_id, lim=limit,
        )
    else:
        rows = await db.fetch_all(
            cols + "ORDER BY started_at DESC LIMIT :lim", lim=limit,
        )
    return {"ok": True, "count": len(rows), "calls": rows}


@router.post("/dial", dependencies=[fastapi.Depends(_require_session)])
async def ui_dial(request: Request, payload: dict) -> dict:
    """Dispatch from the browser. Tenant_id is forced from the session —
    a tenant can't dial on someone else's behalf even by crafting payload."""
    from .bridge import dispatch_gradbot_call

    role = _current_role(request)
    tenant_id = role_tenant_id(role)

    raw_to = payload.get("to", "")
    normalised_to = "+" + "".join(ch for ch in str(raw_to) if ch.isdigit())
    if normalised_to == "+":
        return {"ok": False, "error": "destination must contain digits"}

    if tenant_id is not None:
        ok, reason = await ratelimit.reserve(tenant_id)
        if not ok:
            return {"ok": False, "error": reason}

    spec = BusinessCallSpec(
        task=payload.get("reason", ""),
        language=(payload.get("language") or "en").lower(),
        business_name=payload.get("business_name", ""),
        destination=normalised_to,
        allow_booking=bool(payload.get("allow_booking", False)),
    )
    out = await dispatch_gradbot_call(to=normalised_to, spec=spec, tenant_id=tenant_id)
    if out.startswith("Error:"):
        ratelimit.release(tenant_id)
        return {"ok": False, "error": out}
    return {"ok": True, "room": out}


@router.get("/audio/{room}", dependencies=[fastapi.Depends(_require_session)])
async def ui_audio(request: Request, room: str):
    from .bridge import RECORDINGS_ROOT

    role = _current_role(request)
    tenant_id = role_tenant_id(role)

    safe = "".join(ch for ch in room if ch.isalnum() or ch in "_-")
    if not safe or safe != room:
        raise fastapi.HTTPException(status_code=400, detail="bad room")

    if tenant_id is not None:
        call = await tenants.get_call(safe)
        if not call or call.get("tenant_id") != tenant_id:
            raise fastapi.HTTPException(status_code=404, detail="not yours")

    base = RECORDINGS_ROOT / safe
    for candidate in ("mixed.wav", "tts_direct.wav", "sip_caller.wav"):
        path = base / candidate
        if path.exists():
            return FileResponse(path, media_type="audio/wav", filename=f"{safe}-{candidate}")
    raise fastapi.HTTPException(status_code=404, detail="no audio yet")


# ─── Voice clone (tenant or operator) ─────────────────

@router.get("/voice", dependencies=[fastapi.Depends(_require_session)])
async def ui_voice_status(request: Request) -> dict:
    """Return the tenant's current voice clone metadata, or None."""
    role = _current_role(request)
    tenant_id = role_tenant_id(role)
    if tenant_id is None:
        return {"ok": True, "voice_id": None, "voice_name": None, "scope": "operator"}
    t = await tenants.get_tenant_by_id(tenant_id)
    return {
        "ok": True,
        "voice_id": (t or {}).get("voice_id"),
        "voice_name": (t or {}).get("voice_name"),
        "scope": "tenant",
    }


@router.post("/voice", dependencies=[fastapi.Depends(_require_session)])
async def ui_voice_upload(request: Request, audio: fastapi.UploadFile = fastapi.File(...)):
    """Upload an audio sample, create a Gradium voice clone, attach to tenant.

    Operator uploads aren't attached anywhere — they just return the UID for
    the operator to wire elsewhere. Tenant uploads replace any existing
    voice on their account.
    """
    role = _current_role(request)
    tenant_id = role_tenant_id(role)

    blob = await audio.read()
    suffix = Path(audio.filename or "audio.bin").suffix or ".ogg"
    label = (audio.filename or "voice").rsplit(".", 1)[0][:60]

    if tenant_id is not None:
        t = await tenants.get_tenant_by_id(tenant_id)
        clone_name = f"gradphone:{t['name']}" if t else f"gradphone:tenant-{tenant_id}"
    else:
        clone_name = f"gradphone:operator:{label}"

    try:
        result = await voices.clone_from_bytes(
            blob, name=clone_name, suffix=suffix,
            description="Voice clone from gradphone upload",
        )
    except Exception as e:  # noqa: BLE001
        log.exception("voice clone failed")
        return JSONResponse({"ok": False, "error": str(e)}, status_code=400)

    uid = result.get("uid") or result.get("voice_id") or result.get("id")
    if not uid:
        return JSONResponse({"ok": False, "error": f"unexpected Gradium response: {result}"}, status_code=502)

    if tenant_id is not None:
        await tenants.set_tenant_voice(tenant_id, uid, voice_name=clone_name)

    return {"ok": True, "voice_id": uid, "voice_name": clone_name, "raw": result}


@router.post("/voice/clear", dependencies=[fastapi.Depends(_require_session)])
async def ui_voice_clear(request: Request) -> dict:
    """Remove the tenant's voice clone — calls revert to the language default."""
    role = _current_role(request)
    tenant_id = role_tenant_id(role)
    if tenant_id is None:
        return {"ok": False, "error": "operators have no per-account voice"}
    t = await tenants.get_tenant_by_id(tenant_id)
    old_uid = (t or {}).get("voice_id")
    await tenants.set_tenant_voice(tenant_id, "", "")
    # Best-effort: delete the clone server-side. Non-fatal on failure.
    if old_uid:
        await voices.delete_voice(old_uid)
    return {"ok": True, "cleared": bool(old_uid)}


# ─── Tenant management (operator only) ────────────────

@router.get("/tenants", dependencies=[fastapi.Depends(_require_operator)])
async def ui_tenants_list() -> dict:
    rows = await tenants.list_tenants()
    # Annotate each tenant with today's call count.
    out = []
    for t in rows:
        today = await tenants.count_calls_today(t["id"])
        in_flight = ratelimit.in_flight_count(t["id"])
        out.append({**t, "calls_today": today, "in_flight": in_flight,
                    "effective_quota": t.get("custom_calls_per_day") or ratelimit.DEFAULT_DAILY_QUOTA})
    return {"ok": True, "count": len(out), "tenants": out}


@router.post("/tenants/{tid}/update", dependencies=[fastapi.Depends(_require_operator)])
async def ui_tenant_update(tid: int, payload: dict) -> dict:
    """Set is_active and/or custom_calls_per_day for a tenant."""
    fields = []
    params: dict = {}
    if "is_active" in payload:
        fields.append("is_active = :is_active")
        params["is_active"] = 1 if payload["is_active"] else 0
    if "custom_calls_per_day" in payload:
        v = payload["custom_calls_per_day"]
        if v in (None, "", 0):
            fields.append("custom_calls_per_day = NULL")
        else:
            try:
                params["ccpd"] = int(v)
                fields.append("custom_calls_per_day = :ccpd")
            except (TypeError, ValueError):
                return {"ok": False, "error": "custom_calls_per_day must be an int or null"}
    if not fields:
        return {"ok": False, "error": "nothing to update"}
    params["id"] = tid
    await db.execute(f"UPDATE tenants SET {', '.join(fields)} WHERE id = :id", **params)
    return {"ok": True}
